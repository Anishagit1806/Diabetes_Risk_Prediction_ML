# ==========================================
# 1. Import Libraries
# ==========================================
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import (
    accuracy_score,
    classification_report,
    confusion_matrix,
    roc_curve,
    auc
)

# ==========================================
# 2. Load Dataset
# ==========================================
df = pd.read_csv("Diabetespred.csv")

print("Dataset Preview")
print(df.head())

# ==========================================
# 3. Data Preprocessing
# ==========================================

cols = ['Glucose', 'BloodPressure', 'SkinThickness',
        'Insulin', 'BMI']

for col in cols:
    df[col] = df[col].replace(0, np.nan)
    df[col].fillna(df[col].median(), inplace=True)

df.drop_duplicates(inplace=True)

print("\nMissing Values")
print(df.isnull().sum())

# ==========================================
# 4. Exploratory Data Analysis
# ==========================================

# ---------------------------
# Univariate Analysis
# ---------------------------

plt.figure(figsize=(6,4))
plt.hist(df['Glucose'], bins=20)
plt.title("Glucose Distribution")
plt.xlabel("Glucose")
plt.ylabel("Frequency")
plt.show()

# ---------------------------
# Bivariate Analysis
# ---------------------------

plt.figure(figsize=(6,4))
sns.boxplot(x='Outcome', y='Glucose', data=df)
plt.title("Glucose vs Outcome")
plt.show()

plt.figure(figsize=(6,4))
sns.boxplot(x='Outcome', y='BMI', data=df)
plt.title("BMI vs Outcome")
plt.show()

# ---------------------------
# Correlation Heatmap
# ---------------------------

plt.figure(figsize=(8,6))
sns.heatmap(df.corr(), annot=True, cmap='coolwarm')
plt.title("Correlation Heatmap")
plt.show()

# ==========================================
# 5. Feature Selection
# ==========================================

X = df[['Glucose', 'BMI', 'Age', 'BloodPressure']]
y = df['Outcome']

# ==========================================
# 6. Train Test Split
# ==========================================

X_train, X_test, y_train, y_test = train_test_split(
    X,
    y,
    test_size=0.20,
    random_state=42
)

# ==========================================
# 7. Feature Scaling
# ==========================================

scaler = StandardScaler()

X_train = scaler.fit_transform(X_train)
X_test = scaler.transform(X_test)

# ==========================================
# 8. Logistic Regression Model
# ==========================================

model = LogisticRegression()

model.fit(X_train, y_train)

# ==========================================
# 9. Prediction
# ==========================================

y_pred = model.predict(X_test)

print("\nAccuracy Score")
print(accuracy_score(y_test, y_pred))

print("\nClassification Report")
print(classification_report(y_test, y_pred))

# ==========================================
# 10. Confusion Matrix
# ==========================================

cm = confusion_matrix(y_test, y_pred)

plt.figure(figsize=(5,4))
sns.heatmap(cm,
            annot=True,
            fmt='d',
            cmap='Blues')

plt.title("Confusion Matrix")
plt.xlabel("Predicted")
plt.ylabel("Actual")
plt.show()

# ==========================================
# 11. ROC Curve
# ==========================================

y_prob = model.predict_proba(X_test)[:, 1]

fpr, tpr, thresholds = roc_curve(y_test, y_prob)

roc_auc = auc(fpr, tpr)

plt.figure(figsize=(6,4))
plt.plot(fpr, tpr,
         label=f"AUC = {roc_auc:.2f}")

plt.plot([0,1],[0,1],'--')

plt.xlabel("False Positive Rate")
plt.ylabel("True Positive Rate")
plt.title("ROC Curve")
plt.legend()
plt.show()

# ==========================================
# 12. Feature Importance
# ==========================================

importance = pd.DataFrame({
    'Feature': ['Glucose', 'BMI',
                'Age', 'BloodPressure'],
    'Coefficient': model.coef_[0]
})

importance = importance.sort_values(
    by='Coefficient',
    ascending=False
)

print("\nFeature Importance")
print(importance)

plt.figure(figsize=(6,4))
sns.barplot(
    x='Coefficient',
    y='Feature',
    data=importance
)
plt.title("Feature Importance")
plt.show()

# ==========================================
# 13. Risk Status Classification
# ==========================================

df['Prediction'] = model.predict(
    scaler.transform(X)
)

def risk_status(row):

    if row['Prediction'] == 0 and row['Glucose'] < 110:
        return "Low Risk"

    elif row['Prediction'] == 0:
        return "Medium Risk"

    else:
        return "High Risk"

df['Risk_Status'] = df.apply(
    risk_status,
    axis=1
)

print("\nRisk Prediction Sample")
print(
    df[['Glucose',
        'BMI',
        'Prediction',
        'Risk_Status']].head()
)

# ==========================================
# 14. Save Output
# ==========================================

df.to_csv(
    "Diabetes_Output.csv",
    index=False
)

print("\nOutput file saved successfully!")

